package de.christophlorenz.wmmusic.tools.bean;

public class InventoryBean {
	
	private String singles;
	private String lps;
	private String cds;
	/**
	 * @return the singles
	 */
	public String getSingles() {
		return singles;
	}
	/**
	 * @param singles the singles to set
	 */
	public void setSingles(String singles) {
		this.singles = singles;
	}
	/**
	 * @return the lps
	 */
	public String getLps() {
		return lps;
	}
	/**
	 * @param lps the lps to set
	 */
	public void setLps(String lps) {
		this.lps = lps;
	}
	/**
	 * @return the cds
	 */
	public String getCds() {
		return cds;
	}
	/**
	 * @param cds the cds to set
	 */
	public void setCds(String cds) {
		this.cds = cds;
	}
	
	

}
